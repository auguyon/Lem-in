long long	ft_atoll(const char *str)
{
	long long	r;
	short		n;

	n = 0;
	r = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '-')
		n = 1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str >= 48 && *str <= 57)
	{
		r = r * 10 + (*str - '0');
		str++;
	}
	return (n == 1 ? -r : r);
}
