unsigned int	ft_utoi(const char *str)
{
	unsigned int	r;

	r = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
		str++;
	while (*str >= 48 && *str <= 57)
	{
		r = r * 10 + (*str - '0');
		str++;
	}
	return (r);
}
